namespace Calculator
{
    public static class BelajarMenghitung
    {
        public static int Tambah(int input1, int input2)
        {
            return input1 + input2;
        }

        public static int Kurang(int input1, int input2)
        {
            return input1 - input2;
        }

        public static int Bagi(int input1, int input2)
        {
            return input1 / input2;
        }
    }
}